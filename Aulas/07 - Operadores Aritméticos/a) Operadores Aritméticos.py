# Declarando variáveis
a = 10
b = 3

# Adição
soma = a + b
print("A soma de", a, "e", b, "é igual a", soma)

# Subtração
subtracao = a - b
print("A diferença entre", a, "e", b, "é igual a", subtracao)

# Multiplicação
multiplicacao = a * b
print("O produto de", a, "e", b, "é igual a", multiplicacao)

# Divisão
divisao = a / b
print("O quociente da divisão de", a, "por", b, "é igual a", divisao)

# Divisão inteira
divisao_inteira = a // b
print("A parte inteira da divisão de", a, "por", b, "é igual a", divisao_inteira)

# Resto da divisão
resto_divisao = a % b
print("O resto da divisão de", a, "por", b, "é igual a", resto_divisao)

# Potência
potencia = a ** b
print(a, "elevado a", b, "é igual a", potencia)
